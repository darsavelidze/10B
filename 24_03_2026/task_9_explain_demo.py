# открываем файл для чтения
f = open('9_23747.csv')

# проходим по каждой строке файла
for line in f:

    # разбиваем строку по пробелам и превращаем элементы в числа
    # например: "1 2 2 3 3 3 4" → [1, 2, 2, 3, 3, 3, 4]
    s = [int(x) for x in line.split()]

    # создаём список, где для каждого элемента указано,
    # сколько раз он встречается в списке s
    # пример:
    # s = [0, 5, 5, 4, 4, 4]
    # povts = [1, 2, 2, 3, 3, 3]
    povts = [s.count(x) for x in s]

    # проверяем условие:
    # povts.count(1) == 4 → ровно 4 числа встречаются 1 раз (уникальные)
    # povts.count(3) == 3 → есть число, которое встречается 3 раза
    # (оно даёт три тройки в списке povts)
    if povts.count(1) == 4 and povts.count(3) == 3:

        # список повторяющихся чисел (те, которые встречаются больше 1 раза)
        # пример: [5, 5, 4, 4, 4]
        p = [x for x in s if s.count(x) > 1]

        # список уникальных чисел (встречаются ровно 1 раз)
        # пример: [1, 4]
        n = [x for x in s if s.count(x) == 1]

        # считаем среднее арифметическое уникальных чисел
        # sum(n) → сумма
        # len(n) → количество
        # p[0] → первое повторяющееся число
        # (все элементы p одинаковые по условию задачи)
        if sum(n) / len(n) <= p[0]:
            # выводим:
            # s — исходный список чисел
            # povts — сколько раз встречается каждое число
            # p — повторяющиеся числа
            # n — уникальные числа
            # sum(s) — сумма всех чисел
            print(s, povts, p, n, sum(s))